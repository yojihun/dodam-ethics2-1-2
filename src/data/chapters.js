const makeBlankItem = (text) => ({
  text,
  answers: [...text.matchAll(/\[(.*?)\]/g)].map((match) => match[1]),
});

const createDefaultBlankTemplates = (section) => [
  `${section.title}에서는 [${section.details?.[0] ?? section.concepts[0]}]와 [${section.details?.[1] ?? section.concepts[1]}]를 중심으로 학습한다.`,
  `${section.objective.replace("설명할 수 있다.", "[설명]할 수 있다.").replace("이해할 수 있다.", "[이해]할 수 있다.")}`,
  `${section.concepts[0]}와 ${section.concepts[1]}의 뜻과 관계를 자신의 말로 [정리]할 수 있어야 한다.`,
];

const getSectionCardTerms = (section) => {
  const override = sectionOverrides[section.id];
  const preferredTerms = cardConceptOrderBySection[section.id] ?? [];
  if (override?.cardTerms?.length) {
    return override.cardTerms;
  }
  const discoveredTerms = [
    ...preferredTerms,
    ...section.concepts,
    ...(section.details ?? []),
    ...section.concepts.flatMap((concept) => conceptKeywords[concept] ?? []),
  ];

  return discoveredTerms.filter((term, termIndex, array) => array.indexOf(term) === termIndex);
};

const makeSubsection = (section, index) => {
  const override = sectionOverrides[section.id];
  const blankTemplates =
    override?.blankTemplates ??
    blankTemplatesBySection[section.id] ??
    createDefaultBlankTemplates(section);
  const flashcardTerms = getSectionCardTerms(section);

  const flashcards = flashcardTerms.map((term) => ({
    term,
    definition:
      conceptDefinitions[term] ??
      conceptRelatedStatement[term] ??
      `${term}은(는) ${section.title}에서 이해해야 할 핵심 개념이다.`,
    sectionTitle: section.title,
    type: "definition",
  }));

  return {
    id: `${section.id}-1`,
    title: section.title,
    page: `정리 ${index + 1}`,
    objective: section.objective,
    keyPoints: [
      ...((override?.keyPoints ?? null)
        ? override.keyPoints
        : (section.details ?? section.concepts).map(
        (detail) => `${section.title}의 핵심 내용: ${detail}`
      )),
      section.objective,
      `${section.title}의 내용을 바탕으로 학습목표를 자신의 말로 설명할 수 있어야 한다.`,
    ],
    fillInTheBlanks: blankTemplates.map(makeBlankItem),
    flashcards,
    reflectionPrompts: [
      `${section.title}를 공부한 뒤 가장 중요하다고 생각한 개념은 무엇인가요?`,
      `${section.objective}`,
    ],
  };
};

const createSectionQuizzes = (chapter, section) =>
  sectionOverrides[section.id]?.quizzes?.map((quiz, index) => ({
    id: `${chapter.id}-quiz-${section.id}-${index + 1}`,
    chapterId: chapter.id,
    subsectionId: `${section.id}-1`,
    question: quiz.question,
    options: quiz.options,
    answer: quiz.answer,
    explanation: quiz.explanation,
  })) ??
  getSectionCardTerms(section).slice(0, 6).map((concept, index) => {
    const correctDefinition =
      conceptDefinitions[concept] ??
      `${section.title} 단원에서 꼭 이해해야 하는 개념이다.`;
    const distractors = chapterBlueprints
      .flatMap((item) => item.sections.flatMap((subsection) => getSectionCardTerms(subsection)))
      .filter((item) => item !== concept)
      .slice(0, 3);
    const options = shuffleArray([concept, ...distractors]);

    return {
      id: `${chapter.id}-quiz-${section.id}-${index + 1}`,
      chapterId: chapter.id,
      subsectionId: `${section.id}-1`,
      question: `다음 설명으로 가장 알맞은 도덕 개념은 무엇인가?\n"${correctDefinition}"`,
      options,
      answer: options.indexOf(concept),
      explanation: `${concept}은(는) ${section.title}에서 다루는 핵심 개념이다.`,
    };
  });

const createSectionSubjectives = (chapter, section) =>
  sectionOverrides[section.id]?.subjectives?.map((quiz, index) => ({
    id: `${chapter.id}-subq-${section.id}-${index + 1}`,
    subsectionId: `${section.id}-1`,
    question: quiz.question,
    expectedAnswer: quiz.expectedAnswer,
  })) ??
  getSectionCardTerms(section).slice(0, 3).map((concept, index) => ({
    id: `${chapter.id}-subq-${section.id}-${index + 1}`,
    subsectionId: `${section.id}-1`,
    question: `다음 개념의 뜻을 쓰고, ${section.title}에서 왜 중요한지 서술하시오. (${concept})`,
    expectedAnswer: `${concept}은(는) ${conceptDefinitions[concept]} 그리고 ${section.title}에서는 이 개념을 바탕으로 ${section.objective}`,
  }));

const shuffleArray = (items) => {
  const next = [...items];

  for (let index = next.length - 1; index > 0; index -= 1) {
    const randomIndex = Math.floor(Math.random() * (index + 1));
    [next[index], next[randomIndex]] = [next[randomIndex], next[index]];
  }

  return next;
};

export const chapters = chapterBlueprints.map((chapter) => ({
  id: chapter.id,
  title: chapter.title,
  subtitle: chapter.subtitle,
  examTitle,
  pageRange: `${chapter.title} / ${chapter.subtitle}`,
  summary: chapter.summary,
  sections: chapter.sections.map((section, index) => ({
    id: section.id,
    title: section.title,
    subsections: [makeSubsection(section, index)],
  })),
  subjectiveQuizzes: chapter.sections.flatMap((section) =>
    createSectionSubjectives(chapter, section)
  ),
}));

export const quizzes = chapterBlueprints.flatMap((chapter) =>
  chapter.sections.flatMap((section) => createSectionQuizzes(chapter, section))
);
